print(x,"|",y,"adalah", x|y, ", Binary :",format(x|y,"04b"))
print("===========================================================")

print(x,">>",y,"adalah", x>>y, ", Binary :",format(x>>y,"04b"))
print("===========================================================")

print(x,"^",y,"adalah", x^y, ", Binary :",format(x^y,"04b"))
print("===========================================================")

print("~",x,"adalah", ~x, ", Binary :",format(~x,"04b"))
print("===========================================================")

print(x,"&",y,"adalah", x&y, ", Binary :",format(x&y,"04b"))


